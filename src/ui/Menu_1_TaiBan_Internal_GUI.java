    private void jButton_enterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_enterActionPerformed
        String tableCode = jLabel_code_empty.getText();
        String tableName = jLabel_name_empty.getText();
        String trangThai = "Hoạt động";
        try {
            ban_DAO.updateStatus(trangThai, tableName);
        } catch (SQLException ex) {
            Logger.getLogger(Menu_1_TaiBan_Internal_GUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        displayTables();
        jDialog_table_empty.dispose();
        jPanel_container.removeAll();

        Dashboard_GUI dashboard_GUI = new Dashboard_GUI();
        Menu_2_TaiQuay_Internal_GUI menu_2 = new Menu_2_TaiQuay_Internal_GUI();
        
        menu_2.jLabel_currentUS.setText(jLabel_currentUS.getText());
        menu_2.jLabel_table_codes.setText(tableCode);
        menu_2.jLabel_table_name.setText(tableName);
        jPanel_container.add(menu_2).setVisible(true);
        
        jPanel_container.revalidate();
        jPanel_container.repaint();
        jDialog_table_empty.dispose();
        
    }//GEN-LAST:event_jButton_enterActionPerformed

