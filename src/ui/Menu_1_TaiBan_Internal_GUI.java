    private void displayTables() {
        ArrayList<Ban> danhSachBan = ban_DAO.getAllTableBan();
        jPanel_table_Grid.removeAll();
        for (Ban ban : danhSachBan) {
            JPanel tablePanel = createTablePanel(ban);
            // Thêm sự kiện click cho mỗi JPanel sản phẩm
            tablePanel.addMouseListener(new MouseAdapter() {
                @Override
                public void mouseClicked(MouseEvent e) {
                    String code = ban.getMaBan();
                    String name = ban.getTenBan();
                    String status = ban.getTrangThaiBan();
                    int quantity = ban.getSoLuongNguoi();
                    
                    jDialog_table_empty.setLocationRelativeTo(null);
                    jDialog_table_empty.setSize(350, 350);
                    jDialog_table_active.setLocationRelativeTo(null);
                    jDialog_table_active.setSize(350, 350);
                    jDialog_table_reserve.setLocationRelativeTo(null);
                    jDialog_table_reserve.setSize(350, 350);
                    //empty
                    jLabel_code_empty.setText(code);
                    jLabel_name_empty.setText(name);
                    jLabel_detail_empty.setText(status + " - " + quantity + " người");
                    //active
                    jLabel_code_active.setText(code);
                    jLabel_name_active.setText(name);
                    jLabel_detail_active.setText(status + " - " + quantity + " người");
                    //reserve
                    jLabel_code_reserve.setText(code);
                    jLabel_name_reserve.setText(name);
                    jLabel_detail_reserve.setText(status + " - " + quantity + " người");
                    if (status.equals("Trống")) {
                        jDialog_table_empty.setVisible(true);
                    } else if (status.equals("Hoạt động")) {
                        jDialog_table_active.setVisible(true);
                    } else {
                        jDialog_table_reserve.setVisible(true);
                    }

                    jDialog_table_empty.setLocationRelativeTo(null);
                    jDialog_table_empty.setSize(300, 300);

                }
            });

            jPanel_table_Grid.add(tablePanel);
        }

        jPanel_table_Grid.revalidate();
        jPanel_table_Grid.repaint();
    }

    public JPanel createTablePanel(Ban ban) {
        JPanel tablePanel = new JPanel(new BorderLayout());
        tablePanel.setBorder(javax.swing.BorderFactory.createEmptyBorder(10, 10, 10, 10));
        // Ten <3
        JLabel codeLabel = new JLabel(ban.getMaBan());
        codeLabel.setHorizontalAlignment(SwingConstants.CENTER);
        codeLabel.setFont(new Font("Arial", Font.BOLD, 15));
        JLabel nameLabel = new JLabel(ban.getTenBan());
        nameLabel.setHorizontalAlignment(SwingConstants.CENTER);
        nameLabel.setFont(new Font("Arial", Font.BOLD, 15));
        JLabel nvLabel = new JLabel();
        nvLabel.setHorizontalAlignment(SwingConstants.CENTER);
        nvLabel.setFont(new Font("Arial", Font.BOLD, 15));
        JPanel jPanel_heading = new JPanel(new GridLayout(1, 2));
        jPanel_heading.add(codeLabel);
        jPanel_heading.add(nvLabel);
        jPanel_heading.add(nameLabel);
        // Trang Thai <3
        JLabel statusLabel = new JLabel();
        statusLabel.setHorizontalAlignment(SwingConstants.CENTER);
        // So Luong <3
        JLabel quantityLabel = new JLabel(String.valueOf(ban.getTrangThaiBan() + " - " + ban.getSoLuongNguoi() + " người"));
        quantityLabel.setHorizontalAlignment(SwingConstants.CENTER);
        quantityLabel.setFont(new Font("Arial", Font.BOLD, 15));
        //
        tablePanel.add(jPanel_heading, BorderLayout.NORTH);
        tablePanel.add(statusLabel, BorderLayout.CENTER);
        tablePanel.add(quantityLabel, BorderLayout.SOUTH);

        tablePanel.setPreferredSize(new Dimension(tablePanel.getPreferredSize().width, 150));

        if (ban.getTrangThaiBan().equals("Trống")) {
            tablePanel.setBackground(Color.WHITE);
            jPanel_heading.setBackground(Color.WHITE);
            statusLabel.setIcon(new javax.swing.ImageIcon(getClass().getResource("/icon/table-48.png")));
        } else if (ban.getTrangThaiBan().equals("Đặt trước")) {
            tablePanel.setBackground(Color.LIGHT_GRAY);
            jPanel_heading.setBackground(Color.LIGHT_GRAY);
            statusLabel.setIcon(new javax.swing.ImageIcon(getClass().getResource("/icon/lock-48.png")));
        } else {
            tablePanel.setBackground(new Color(0, 102, 54));
            jPanel_heading.setBackground(new Color(0, 102, 54));
            codeLabel.setForeground(Color.WHITE);
            nameLabel.setForeground(Color.WHITE);
            statusLabel.setIcon(new javax.swing.ImageIcon(getClass().getResource("/icon/users-round-48.png")));
            quantityLabel.setForeground(Color.WHITE);
        }
        //
        return tablePanel;
    }

