    private void jTable_customerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_customerMouseClicked
        jDesktopPane_information.removeAll();
        jDesktopPane_information.revalidate();
        jDesktopPane_information.repaint();

        jDesktopPane_information.add(jInternalFrame_editCustomer).setVisible(true);
        jInternalFrame_editCustomer.setSize(jDesktopPane_information.getSize());

        int rowIndex = this.jTable_customer.getSelectedRow();
        String ma = this.jTable_customer.getValueAt(rowIndex, 0).toString();
        String name = this.jTable_customer.getValueAt(rowIndex, 1).toString();
        String phone = this.jTable_customer.getValueAt(rowIndex, 2).toString();
        String gender = this.jTable_customer.getValueAt(rowIndex, 3).toString();
        String address = this.jTable_customer.getValueAt(rowIndex, 4).toString();

        jLabel_name_title.setText(name);
        jLabel_phone_title.setText(phone);
        jTextField_maKH.setText(ma);
        jTextField_name.setText(name);
        jTextField_phone.setText(phone);
        if (gender.equals("Nam")) {
            jRadioButton_male.setSelected(true);
            jRadioButton_female.setSelected(false);
        } else {
            jRadioButton_male.setSelected(false);
            jRadioButton_female.setSelected(true);
        }
        jTextField_address.setText(address);
        //
        jButton_delete.setEnabled(true);
        jButton_delete.setBackground(new Color(204, 51, 0));
        jButton_save.setEnabled(true);
        jButton_save.setBackground(Color.YELLOW);
    }//GEN-LAST:event_jTable_customerMouseClicked

