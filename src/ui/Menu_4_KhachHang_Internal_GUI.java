    public void DocDuLieuDatabaseVaoTable() {
        List<KhachHang> list = khachHang_DAO.getAllTableKhachHang();
        DefaultTableModel model = (DefaultTableModel) jTable_customer.getModel();
        model.setRowCount(0);
        for (KhachHang kh : list) {
            model.addRow(new Object[]{kh.getMaKH(), kh.getTenKH(),
                kh.getSoDienThoaiKH(), kh.isGioiTinhKH()?"Nam":"Nữ", kh.getDiaChiKH()});
        }
    }
    
