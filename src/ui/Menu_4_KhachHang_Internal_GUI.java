    private void jButton_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_deleteActionPerformed
        //dbs
        String maKH = jTextField_maKH.getText().toString();
        String tenKH = jTextField_name.getText().trim();
        try {
            khachHang_DAO.deleteCustomer(maKH);
            JOptionPane.showMessageDialog(this, "Đã xoá " + tenKH + " ra danh mục khách hàng", "Success", JOptionPane.WARNING_MESSAGE);
            jTextField_maKH.setText("");
            jTextField_name.setText("");
            jTextField_phone.setText("");
            jRadioButton_male.setSelected(true);
            jRadioButton_female.setSelected(false);
            jTextField_address.setText("");
            jButton_delete.setEnabled(false);
            jButton_save.setEnabled(false);
            jButton_delete.setBackground(Color.LIGHT_GRAY);
            jButton_save.setBackground(Color.LIGHT_GRAY);
        } catch (Exception e2) {
            JOptionPane.showMessageDialog(this, "Xoá khách hàng không thành công!", "Fail", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton_deleteActionPerformed

