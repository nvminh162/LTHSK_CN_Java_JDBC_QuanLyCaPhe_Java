    ActionListener buttonListener = new ActionListener() {
        @Override
        public void actionPerformed(ActionEvent e) {
            JButton clickedButton = (JButton) e.getSource();
            String buttonText = clickedButton.getText();
            if (buttonText.equals("Nhập lại")) {
                focusedTextField.setText("0");
            } else if (buttonText.equals("Hoàn tất")) {
                return;
            } else {
                if (focusedTextField != null) {
                    String currentText = focusedTextField.getText().replace(",", "");
                    try {
                        int buttonInt = Integer.parseInt(buttonText);
                        long newValue = Long.parseLong(currentText + buttonInt);
                        if (focusedTextField.equals(jTextField_cashValue)) {
                            focusedTextField.setText(decimalFormat.format(newValue));
                        } else {
                            if (newValue <= 100) {
                                focusedTextField.setText(decimalFormat.format(newValue));
                            } else {
                                focusedTextField.setText("0");
                            }
                        }
                    } catch (NumberFormatException ex) {

                    }
                }
            }
        }
    };

    FocusListener textFieldFocusListener = new FocusListener() {
        @Override
        public void focusGained(FocusEvent e) {
            focusedTextField = (JTextField) e.getSource();

        }

        @Override
        public void focusLost(FocusEvent e) {
            lostTextField = (JTextField) e.getSource();
            String text = lostTextField.getText().replaceAll(",", ""); // Remove commas for parsing
            if (text.isEmpty()) {
                lostTextField.setText("0");
            } else {
                try {
                    double value = Double.parseDouble(text);
                    String formattedValue = decimalFormat.format(value);
                    lostTextField.setText(formattedValue);
                } catch (NumberFormatException ex) {

                }
            }
        }

    };

