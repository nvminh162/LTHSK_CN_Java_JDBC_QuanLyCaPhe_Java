    private void jTextField_cashValueFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextField_cashValueFocusLost
        if (jTextField_cashValue.getText().isEmpty()) {
            jTextField_cashValue.setText("0");
        }
        try {
            double value = Double.parseDouble(jTextField_cashValue.getText());
            String formattedValue = decimalFormat.format(value);
            jTextField_cashValue.setText(formattedValue);
        } catch (NumberFormatException ex) {
            jTextField_cashValue.setText("0");
        }
    }//GEN-LAST:event_jTextField_cashValueFocusLost

