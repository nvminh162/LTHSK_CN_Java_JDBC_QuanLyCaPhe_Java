    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        double total = Double.parseDouble(jLabel_temporary.getText().replace(".0", ""));
        double cash = Double.parseDouble(jTextField_cashValue.getText().replace(",", ""));
        if (cash > total) {
            Locale localeVN = new Locale("vi", "VN");
            NumberFormat numf = NumberFormat.getCurrencyInstance(localeVN);
            jTextField11.setText(numf.format(cash - total));
        } else {
            jTextField11.setText("Khách đưa thiếu tiền");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

